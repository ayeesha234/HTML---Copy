# Define two numbers
num1 = 10
num2 = 5

# Perform addition and assign the result to the variable 'addition'
addition = num1 + num2

# Print the result
print("Addition =", addition)

# You can also demonstrate other operations like this:
subtraction = num1 - num2
multiplication = num1 * num2
division = num1 / num2

print("Subtraction =", subtraction)
print("Multiplication =", multiplication)
print("Division =", division)
